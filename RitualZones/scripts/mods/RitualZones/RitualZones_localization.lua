local function readable_color_name(value)
	local readable = {}
	for token in string.gmatch(value or "", "[^_]+") do
		local first = string.sub(token, 1, 1)
		local rest = string.sub(token, 2)
		readable[#readable + 1] = string.upper(first) .. rest
	end
	return table.concat(readable, " ")
end

if Color and Color.list then
	for _, color_name in ipairs(Color.list) do
		if localization[color_name] == nil then
			local ok, values = pcall(Color[color_name], 255, true)
			if ok and values then
				local text = string.format(
					"{#color(%d,%d,%d)}%s{#reset()}",
					values[2],
					values[3],
					values[4],
					readable_color_name(color_name)
				)
				localization[color_name] = { en = text }
			end
		end
	end
end

